		//erhaltene xp hinzufügen
		public function add_xp($action,$action_data,$xp_uuid)
		{

			//$xp_for_action finden
			if($action < 100){
				//add normal xp
				$xp_for_action = "xp_for_".$action;
				$xp_for_action = $this->$xp_for_action;
			}elseif($action >= 100){
				//add Achivement level
				$xp_for_action = "ach_".$action;
				$xp_for_action = $this->$xp_for_action;
			}

			$xp_uuif = sha1(sha1($xp_uuid));

			//wurde xp schon mal hizugefügt
			$sql_xp_bank = db::$link->query("SELECT COUNT(xp_id) FROM xp_db WHERE uuid = '$xp_uuid' AND action = '$action' AND action_data = '$action_data' AND status = 'public'");
			$get_xp_bank = $sql_xp_bank->fetch_row();

			//wurde xp schon mal entfernt
			$sql_del_xp_bank = db::$link->query("SELECT COUNT(xp_id) FROM xp_db WHERE uuid = '$xp_uuid' AND action = '$action' AND action_data = '$action_data' AND status = 'deleted'");
			$get_del_xp_bank = $sql_del_xp_bank->fetch_row();

			//wenn weder xp hinzugefügt noch entfernt wurden
			if($get_del_xp_bank[0] == "0" AND $get_xp_bank[0] == "0"){


				$oldxp = $this->userin('xp',0,$xp_uuif,'');
				$newxp = $oldxp + $xp_for_action;

				$setnewxp = $this->userinset('xp',$newxp,$xp_uuif);
				$upsetnewxp = "UPDATE user_find_db SET xp = '$newxp' WHERE uuid = '$xp_uuid'"; $upsetnewxp = db::$link->query($upsetnewxp);


				if($setnewxp == true){
					$time = date('H:i:s d-m-Y');
					$time = strtotime($time);

					$add_xp = "INSERT INTO xp_db
								(uuid,action,action_data,xp,time,status)
								VALUES
								('$xp_uuid','$action','$action_data','$xp_for_action','$time','public')";
					$add_xp = db::$link->query($add_xp);

					//update level -> notification level
					$this->update_level($xp_uuid);
				}

			//wenn xp mal entfernt wurden werden diese wieder hinzugefügt
			}elseif($get_del_xp_bank[0] == 1){

				$oldxp = $this->userin('xp',0,$xp_uuif,'');
				$newxp = $oldxp + $xp_for_action;

				$setnewxp = $this->userinset('xp',$newxp,$xp_uuif);
				$upsetnewxp = "UPDATE user_find_db SET xp = '$newxp' WHERE uuid = '$xp_uuid'"; $upsetnewxp = db::$link->query($upsetnewxp);

				if($setnewxp == true){
					$update_xp = "UPDATE xp_db SET status = 'public' WHERE uuid = '$xp_uuid' AND action = '$action' AND action_data = '$action_data' AND status = 'deleted'";
					$update_xp = db::$link->query($update_xp);

					//update level -> notification level
					$this->update_level($xp_uuid);
				}
			}
		}



